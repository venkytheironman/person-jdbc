package com.tyss.person;

public class Person {
	int id;
	String name;
	long phone;
	String email;
	String pass;
	int age;
	@Override
	public String toString() {
		return "Person [id=" + id + ", name=" + name + ", phone=" + phone + ", email=" + email + ", pass=" + pass
				+ ", age=" + age + "]";
	}
	

}
